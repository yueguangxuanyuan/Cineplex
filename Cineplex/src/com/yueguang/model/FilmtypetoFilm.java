package com.yueguang.model;

public class FilmtypetoFilm {
	int id;
	Film film;
	Filmtype filmtype;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public Film getFilm() {
		return film;
	}

	public void setFilm(Film film) {
		this.film = film;
	}

	public Filmtype getFilmtype() {
		return filmtype;
	}

	public void setFilmtype(Filmtype filmtype) {
		this.filmtype = filmtype;
	}

}
